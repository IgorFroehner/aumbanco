    private void jTbVendasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTbVendasMouseClicked
        if (evt.getClickCount() == 2) {
            Compras modelo = new Compras();
            //manda para os edits da tela cadastro e volta para a mesma

            Object obj0 = (jTbVendas.getValueAt(jTbVendas.getSelectedRow(), 0));
            modelo.setId_compra(Integer.parseInt(obj0.toString()));

            Object obj1 = (jTbVendas.getValueAt(jTbVendas.getSelectedRow(), 1));
            modelo.setFornecedor(obj1.toString());

            Object obj2 = (jTbVendas.getValueAt(jTbVendas.getSelectedRow(), 2));
            modelo.setDatac(obj2.toString());

            Object obj3 = (jTbVendas.getValueAt(jTbVendas.getSelectedRow(), 3));
            modelo.setN_notafiscal(obj3.toString());
            
            Object obj4 = (jTbVendas.getValueAt(jTbVendas.getSelectedRow(), 4));
            modelo.setValor_total(Double.parseDouble(obj4.toString()));

            //manda para outra tela com as informações nos edits
            TelaComprar dialog = new TelaComprar(new javax.swing.JFrame(), true);
            Cores cores = new Cores();

            cores.setFundo(jPnFundo.getBackground());
            cores.setMenus(jPnMenu.getBackground());

            dialog.cores(cores);
            dialog.manda(modelo);
            dialog.usuario(usua);
            dialog.setVisible(true);
            dispose();
        }
    }//GEN-LAST:event_jTbVendasMouseClicked

