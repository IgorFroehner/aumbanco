    public void tabela(Compras modelo) {
        //pegando do modelo e mandando para a tabela          
        tabela.addRow(new Object[]{
            modelo.getId_compra(),
            modelo.getFornecedor()+ " " + modelo.getCnpj_cpf(),
            modelo.getDatac(),
            modelo.getN_notafiscal(),
            modelo.getValor_total()
        });
    }

    public void consulta() {
        //instanciando Classe Prova() do Modelo
        Compras modelo = new Compras();

        //mandando pro set do modelo
        modelo.setFornecedor(jTpesquisa.getText());
        modelo.setDatac((String) jSelecionar.getSelectedItem());

        //criando a variavel para a tabela
        tabela = (javax.swing.table.DefaultTableModel) jTbVendas.getModel();
        tabela.setNumRows(0);

        CompraDAO dao = new CompraDAO();

        try {
            //evento do DAO  
            dao.consulta(modelo);

        } catch (HeadlessException | ParseException ex) {
            Logger.getLogger(consProdutos.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

