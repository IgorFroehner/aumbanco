    public void tabela(Usuario modelo) {
        //pegando do modelo e mandando para a tabela          
        tabela.addRow(new Object[]{
            modelo.getNome(),
            modelo.getLogin(),
            modelo.getPermissao(),
        });
    }

    public void consulta() {
        //instanciando Classe Prova() do Modelo
        Usuario modelo = new Usuario();

        //mandando pro set do modelo
        modelo.setLogin(jTpesquisa.getText());
        modelo.setNome((String) jSelecionar.getSelectedItem());

        //criando a variavel para a tabela
        tabela = (javax.swing.table.DefaultTableModel) jTbUsuarios.getModel();
        tabela.setNumRows(0);

        UsuarioDAO dao = new UsuarioDAO();

        try {
            try {
                //evento do DAO
                dao.consultaUsuario(modelo);
            } catch (SQLException ex) {
                Logger.getLogger(consUsuario.class.getName()).log(Level.SEVERE, null, ex);
            }
        } catch (HeadlessException ex) {
            Logger.getLogger(consProdutos.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

