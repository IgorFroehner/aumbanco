    public void carrinho() {

        //criando a mensagem para selecionar quantidade
        JPanel panel = new JPanel();
        panel.setLayout(new GridLayout(2, 1));
        JLabel label = new JLabel();
        label.setFont(new java.awt.Font("Tahoma", 1, 18));
        label.setText("Digite a quantia a ser vendida");
        JSpinner spiner = new JSpinner();
        spiner.setFont(new java.awt.Font("Tahoma", 1, 18));
        spiner.setModel(new javax.swing.SpinnerNumberModel(1, 1, 999999999, 1));
        
        panel.add(label);
        panel.add(spiner);

        //mostrando na tela a mensagem para selecionar a quantidade
        int resultado = JOptionPane.showConfirmDialog(null, panel, "Quantidade", JOptionPane.OK_CANCEL_OPTION);

        if (resultado == JOptionPane.OK_OPTION) {

            //pegando a quantidade
            int qCompra = (int) spiner.getValue();
            if (qCompra == 0) {
                JOptionPane.showMessageDialog(null, "Zero(0) não é uma quantia válida");
                jTpesquisa.requestFocus();
            } else {

                //manda para outra tabela
                carrinho = (javax.swing.table.DefaultTableModel) jTbcarrinho.getModel();

                Object obj1 = (jTbpesquisa.getValueAt(jTbpesquisa.getSelectedRow(), 0));
                int codPesquisa = Integer.parseInt(obj1.toString());

                Object obj2 = (jTbpesquisa.getValueAt(jTbpesquisa.getSelectedRow(), 1));
                String nome = obj2.toString();

                Object obj3 = (jTbpesquisa.getValueAt(jTbpesquisa.getSelectedRow(), 2));
                Double valor = (Double) obj3;

                Double preco_total = valor * qCompra;

                carrinho.addRow(new Object[]{codPesquisa, nome, valor, qCompra, preco_total});

                tabela = (javax.swing.table.DefaultTableModel) jTbpesquisa.getModel();
                tabela.setNumRows(0);
                jTpesquisa.setText("");
                jTpesquisa.requestFocus();

                //Preco total
                Double precoT = pTotal;
                pTotal = (precoT + preco_total);
                jTpreco.setText("" + pTotal);

                jBtAdicionar.setEnabled(false);
            }
        }
    }

    public void tiradocarrinho() {
        Object obj1 = (jTbcarrinho.getValueAt(jTbcarrinho.getSelectedRow(), 4));
        Double valor = (Double) obj1;

        Double precoT = pTotal;
        Double precoP = valor;
        pTotal = (precoT - precoP);
        jTpreco.setText("" + pTotal);

        carrinho = (javax.swing.table.DefaultTableModel) jTbcarrinho.getModel();
        carrinho.removeRow(jTbcarrinho.getSelectedRow());
    }

    public void cadastraVenda() {

        Venda modelo = new Venda();

        if (jCbCliente.getSelectedItem().equals("Selecione")) {
            JOptionPane.showMessageDialog(null, "Selecione um Cliente");
            jCbCliente.requestFocus();
        } else {

            carrinho = (DefaultTableModel) jTbcarrinho.getModel();

            //valida se tem preodutos no carrinho
            if (carrinho.getRowCount() == 0) {
                JOptionPane.showMessageDialog(null, "Selecione ao menos um produto para a venda");
                jTpesquisa.requestFocus();
            } else {

                //metodo para cadastrar a venda
                int resultado = JOptionPane.showConfirmDialog(null, "Tem certeza que deseja confirmar a venda?");
                if (resultado == JOptionPane.YES_OPTION) {
                    VendaDAO dao = new VendaDAO();

                //manda os dados para o modelo
                    //seta CPF do cliente cortando a string para pesquisar o id no banco
                    String cliente = jCbCliente.getSelectedItem().toString();
                    String cortaCpf = cliente.substring(cliente.length() - 14);
                    modelo.setCnpf_cpf(cortaCpf);

                    modelo.setId_venda(Integer.parseInt(jTcod.getText()));
                    modelo.setDatav("" + new SimpleDateFormat("dd/MM/yyyy").format(jDateChooser1.getDate()));
                    modelo.setValorTotal(pTotal);

                    try {
                        dao.cadastra_Venda(modelo);
                    } catch (SQLException | HeadlessException | ParseException ex) {
                        Logger.getLogger(TelaVender.class.getName()).log(Level.SEVERE, null, ex);
                    }

                    //pega numero de linhas da tabela pro while
                    int y = carrinho.getRowCount() - 1;

                    //cadastra todas as linhas da tabela
                    do {

                        //manda variaveis para o modelo
                        modelo.setId_venda(Integer.parseInt(jTcod.getText()));
                        modelo.setId_produto((int) carrinho.getValueAt(y, 0));
                        modelo.setPreco((double) carrinho.getValueAt(y, 2));
                        modelo.setQuantidade((int) carrinho.getValueAt(y, 3));

                        try {
                            dao.cadastra_ProdutosVenda(modelo);
                        } catch (SQLException ex) {
                            Logger.getLogger(TelaVender.class.getName()).log(Level.SEVERE, null, ex);
                        }
                        y = y - 1;
                    } while (!(y < 0));
                    limpar();
                } else {
                    JOptionPane.showMessageDialog(null, "Venda não confirmada");
                }
            }
        }
    }

