    private void jTpesquisaKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTpesquisaKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_DOWN) {
            //manda o foco para o primeiro item da tabela
            jTbpesquisa.requestFocus();
            jTbpesquisa.getSelectionModel().clearSelection();
            jTbpesquisa.getSelectionModel().setSelectionInterval(0, 0);
        }
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            if (jTpesquisa.getText().equals("")) {
                consulta();
            } else {
                //manda o foco para o primeiro item da tabela
                jTbpesquisa.requestFocus();
                jTbpesquisa.getSelectionModel().clearSelection();
                jTbpesquisa.getSelectionModel().setSelectionInterval(0, 0);
            }
        } else {
            consulta();
        }
    }//GEN-LAST:event_jTpesquisaKeyPressed


