    public void manda(Venda modelo) {
        limpacampos();

        jTcod.setText("" + modelo.getId_venda());
        //manda a data para o Date chooser
        try {
            jDateChooser1.setDate(new SimpleDateFormat("dd/MM/yyyy").parse(modelo.getDatav()));
        } catch (ParseException ex) {
            Logger.getLogger(TelaVender.class.getName()).log(Level.SEVERE, null, ex);
        }

        //pesquisa para o combo
        VendaDAO dao = new VendaDAO();
        Pessoas pes = new Pessoas();

        //parte da pesquisa de clientes para o combobox
        comboclientes = jCbCliente;
        comboclientes.removeAllItems();
        comboclientes.addItem("Selecione");
        try {
            dao.consultaCombo(pes);
        } catch (HeadlessException | ParseException ex) {
            Logger.getLogger(TelaVender.class.getName()).log(Level.SEVERE, null, ex);
        }
        //seleciona o combobox
        jCbCliente.setSelectedItem(modelo.getNome_cliente());

        //consulta para tabela produtos venda (CARRINHO)
        try {
            dao.consultaProdutosVenda(modelo);
        } catch (HeadlessException | ParseException ex) {
            Logger.getLogger(TelaVender.class.getName()).log(Level.SEVERE, null, ex);
        }

        jTpreco.setText("" + modelo.getValorTotal());

        jBtNovo.setEnabled(false);
        jBtEditar.setEnabled(true);
        jBtLimpar.setEnabled(true);
    }

