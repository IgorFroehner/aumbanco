    private void jBtCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtCadastrarActionPerformed
        Pessoas modelo = new Pessoas();

        String email = jTemail.getText();

        if (jTnome.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Digite um nome!");
            jTnome.requestFocus();
            return;
        } else if (jTcpf.getText().equals(mascCpf)) {
            JOptionPane.showMessageDialog(null, "Digite um CPF/CNPJ!");
            jTcpf.requestFocus();
            return;
        } else if (validaCpf(jTcpf.getText().replaceAll("[^0-9]", "")) == false) {
            JOptionPane.showMessageDialog(null, "Digite um CPF/CNPJ válido");
            jTcpf.requestFocus();
            return;
        } else if (jCbEstado.getSelectedItem().toString().equals("Selecione")) {
            JOptionPane.showMessageDialog(null, "Selecione um estado!");
            jCbEstado.requestFocus();
            return;
        } else if (jCbCidade.getSelectedItem().toString().equals("Selecione")) {
            JOptionPane.showMessageDialog(null, "Selecione uma cidade!");
            jCbCidade.requestFocus();
            return;
        } else if (jCbCidade.getSelectedItem().toString().equals("Selecione")) {
            JOptionPane.showMessageDialog(null, "Selecione um  estado!");
            jCbCidade.requestFocus();
            return;
        } else if (jTbairro.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Digite um bairro!");
            jTbairro.requestFocus();
            return;
        } else if (jTrua.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Digite uma rua!");
            jTrua.requestFocus();
            return;
        } else if (jTnum.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Digite um número!");
            jTnum.requestFocus();
            return;
            //validacao do email somente se a pessoa digitar
        } else if (!jTemail.getText().equals("")) {
            if (isEmailValid(email) == false) {
                JOptionPane.showMessageDialog(null, "Digite um e-mail válido!");
                jTemail.requestFocus();
                return;
            }
        }
        if (isEmailValid(email) == true || jTemail.getText().equals("")) {

            //se == 1 manda para o evento de salvar edicao
            if (btEditar == 1) {
                salvaEdicao(modelo);
            } else {

                PessoaDAO dao = new PessoaDAO();

                //validacao no banco para ver se nao tem a Primary Key
                modelo.setCPF_CNPJ(jTcpf.getText());
                try {
                    dao.consultaCPF_CNPJ(modelo);
                } catch (HeadlessException | ParseException ex) {
                    Logger.getLogger(cadPessoas.class.getName()).log(Level.SEVERE, null, ex);
                }
                if (!(modelo.getCPF_CNPJ().equals(""))) {
                    JOptionPane.showMessageDialog(null, "CPF já cadastrado no Sistema." + modelo.getCPF_CNPJ());
                    jTcpf.requestFocus();
                } else {

                    //evento para salvar no banco
                    modelo.setCliente_fornecedor(cliente_fornecedor);
                    modelo.setNomePessoa(jTnome.getText());
                    modelo.setTelefone(jTfone.getText());
                    modelo.setCPF_CNPJ(jTcpf.getText());
                    modelo.setEstado(jCbEstado.getSelectedItem().toString());
                    modelo.setCidade(jCbCidade.getSelectedItem().toString());
                    modelo.setBairro(jTbairro.getText());
                    modelo.setRua(jTrua.getText());
                    modelo.setNum(jTnum.getText().replace(".", ""));
                    modelo.setComplemento(jTcomp.getText());
                    modelo.setEmail(jTemail.getText());
                    modelo.setHoraalteracao(new SimpleDateFormat("HH:mm dd/MM/yyyy").format(new Date()));
                    modelo.setUsuario(usua);

                    //CADASTRO NO BANCO
                    int resultado = JOptionPane.showConfirmDialog(null, "Deseja cadastrar o " + cliente_fornecedor + "?");
                    if (resultado == JOptionPane.YES_OPTION) {
                        try {
                            dao.cadastra(modelo);
                        } catch (SQLException ex) {
                            Logger.getLogger(cadPessoas.class.getName()).log(Level.SEVERE, null, ex);
                        }
                        JOptionPane.showMessageDialog(null, "Cadastro realizado com sucesso!\n Cadastrado: " + jTnome.getText() + ".");
                        limpar();
                    } else {
                        JOptionPane.showMessageDialog(null, "Cadastro cancelado.");
                    }
                }
            }
        }
    }//GEN-LAST:event_jBtCadastrarActionPerformed

