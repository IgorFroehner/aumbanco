    public boolean validaCpf(String catacpf) throws HeadlessException {

        System.out.println(catacpf);

        if (cliente_fornecedor.equals("cliente")) {

            if (catacpf.equals("00000000000") || catacpf.equals("11111111111") || catacpf.equals("22222222222")
                    || catacpf.equals("33333333333") || catacpf.equals("44444444444") || catacpf.equals("55555555555")
                    || catacpf.equals("66666666666") || catacpf.equals("77777777777") || catacpf.equals("88888888888")
                    || catacpf.equals("99999999999") || (catacpf.length() != 11)) {
                return false;
            }
            //validar cpf
            if (CNP.isValidCPF(catacpf) == false) {
                return false;
            } else {
                return true;
            }

        } else if (cliente_fornecedor.equals("fornecedor")) {
            if (CNP.isValidCNPJ(catacpf) == false) {
                return false;
            } else {
                return true;
            }
        }
        return true;
    }

