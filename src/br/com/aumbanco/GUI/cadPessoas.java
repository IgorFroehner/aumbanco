    public static boolean isEmailValid(String email) {
        if ((email == null) || (email.trim().length() == 0)) {
            return false;
        }

        String emailPattern = "\\b(^[_A-Za-z0-9-]+(\\.[_A-Za-z0-9-]+)*@([A-Za-z0-9-])+(\\.[A-Za-z0-9-]+)*((\\.[A-Za-z0-9]{2,})|(\\.[A-Za-z0-9]{2,}\\.[A-Za-z0-9]{2,}))$)\\b";
        Pattern pattern = Pattern.compile(emailPattern, Pattern.CASE_INSENSITIVE);
        Matcher matcher = pattern.matcher(email);
        return matcher.matches();
    }

    public void manda(Pessoas modelo) {
        limpacampos();

        if (modelo.getCliente_fornecedor().equals("cliente")) {
            labelCPF();
        } else if (modelo.getCliente_fornecedor().equals("fornecedor")) {
            labelCNPJ();
        }

        PessoaDAO dao = new PessoaDAO();
        dao.estados();
        jCbEstado.setSelectedItem(modelo.getEstado());

        modelo.setCodEstado(jCbEstado.getSelectedIndex());

        jCbCidade.removeAllItems();
        jCbCidade.addItem("Selecione");
        dao.cidade(modelo);
        jCbCidade.setSelectedItem(modelo.getCidade());

        jTnome.setText(modelo.getNomePessoa());
        jTcpf.setText(modelo.getCPF_CNPJ());
        jTfone.setText("" + modelo.getTelefone());
        jTbairro.setText(modelo.getBairro());
        jTrua.setText(modelo.getRua());
        jTnum.setText("" + modelo.getNum());
        jTcomp.setText(modelo.getComplemento());
        jTemail.setText(modelo.getEmail());

        jBtNovo.setEnabled(false);
        jBtEditar.setEnabled(true);
        jBtLimpar.setEnabled(true);
    }

