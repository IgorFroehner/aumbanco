    //Código do relogio da Interface
    public final class horario extends JFrame implements ActionListener {

        private javax.swing.Timer timer;

        public horario() {
            super("Horario");
            disparaRelogio();
        }

        public void disparaRelogio() {
            if (timer == null) {
                timer = new javax.swing.Timer(100, this);
                timer.setInitialDelay(0);
                timer.start();
            } else if (!timer.isRunning()) {
                timer.restart();
            }
        }

        @Override
        public void actionPerformed(ActionEvent ae) {
            Date hora = new Date();
            //estabelecendo formato
            SimpleDateFormat hora_formato = new SimpleDateFormat("HH:mm");
            //mandando para a Label
            jLhora.setText(hora_formato.format(hora));
        }
    }

