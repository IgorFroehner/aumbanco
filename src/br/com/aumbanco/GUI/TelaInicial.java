    public void tabela(Produtos modelo) {
        estoquebaixo.addRow(new Object[]{
            modelo.getNome()});
    }

    public void consultaEstoqueBaixo() {
        Produtos modelo = new Produtos();
        ProdutoDAO dao = new ProdutoDAO();

        estoquebaixo = (DefaultTableModel) jTbEstoqueBaixo.getModel();
        estoquebaixo.setNumRows(0);

        try {
            dao.EstoqueBaixo(modelo);
        } catch (HeadlessException | ParseException ex) {
            Logger.getLogger(TelaInicial.class.getName()).log(Level.SEVERE, null, ex);
        }
        if (estoquebaixo.getColumnCount() > 0) {
            jTbEstoqueBaixo.setBackground(Color.red);
        } else {
            jTbEstoqueBaixo.setBackground(Color.white);
            estoquebaixo.setNumRows(jTbEstoqueBaixo.getHeight());
        }
    }

