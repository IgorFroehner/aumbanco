        //codigo para salvar a saida do usuario do sistema
        Usuario modelo = new Usuario();
        UsuarioDAO dao = new UsuarioDAO();
        Date hora = new Date();
        SimpleDateFormat hora_formato = new SimpleDateFormat("HH:mm");
